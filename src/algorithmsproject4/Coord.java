package algorithmsproject4;

public class Coord {
    private int x;
    private int y;

    public Coord(int xLoc, int yLoc) {
        this.x = xLoc;
        this.y = yLoc;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int xLoc)  {
        this.x = xLoc;
    }

    public void setY(int yLoc)  {
        this.y = yLoc;
    }
}
